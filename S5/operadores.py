#operadores logicos
#and or not
#evaluar booleano True o False
a = True
b = False

print(a and b) #False, si ambos valores son true se imprime true
print(a or b) #True, debe cumplirse alguna de las condiciones
print(not a)#False, Cambia valor de la condicion
print(not b)#True
print(not (a or b))#False
print(not (a and b))#True
print("--------------------------------------")
#OPERADORES DE COMPARACION
#COMPARADORES DE BOOLENOS

c = 10
d = 5

print(c > d) #True, mayor que
print(c < d) #False, menor que
#print(a > b) #True
#print(a < b) #False
print(c <= d) #False, menor o igual que
print(c >= d) #True, mayor o igual que
print(c == d) #False, igual que
print (c != d) #True, distinto que


