#ciclo while
#condicion siempre se evalua true hasta que cambie

i = 0
while i <= 5:
    i +=1 #contador
    print(f"imprimiendo el valor de i {i}")
    
i = 0
while i <= 5:
    i +=1 #contador
    if i == 1:
        continue
    print(f"imprimiendo el valor de i {i} y probando continue")
    

i = 0
while i <= 5:
    i +=1 #contador
    if i == 4:
        break
    print(f"imprimiendo el valor de i {i} y probando break")
    
